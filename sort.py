#using sort to orgranize them in ascending order
alpha = ['c','a','f','b','e']
alpha.sort()
print(alpha)

#using reverse=true to sort them in descending order
alpha.sort(reverse=True)
print(alpha)

#reverse is used to reverse the order of the string given
alpha = ['c','a','f','b','e']
alpha.reverse()
print(alpha)

#using sorted to temporarily sort the list
print(sorted(alpha))
